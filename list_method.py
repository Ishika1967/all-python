# list methods
l1 = [1,8,7,2,21,15]
print(l1)
l1.sort()#sort the original list
print(l1)
l1.reverse()#reverse the original list
print(l1)
l1.append(45)#append the data in the bracket
print(l1)
l1.insert(2,544) # insert 544 at index 2 
print(l1)
l1.pop(2)#will pop the element at 2nd index 
print(l1)
l1.remove(45) # will remove the 45 from the list
print(l1)